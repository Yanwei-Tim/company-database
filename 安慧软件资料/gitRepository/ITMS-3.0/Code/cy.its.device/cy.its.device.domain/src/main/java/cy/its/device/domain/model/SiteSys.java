package cy.its.device.domain.model;

public class SiteSys {
	private String sectionIdList;	//断面ID
	
	private String siteId;	    	//点位ID
	
	private String deviceId;		//设备ID
	
	private String laneId;			//车道ID

	

	

	public String getSectionIdList() {
		return sectionIdList;
	}

	public void setSectionIdList(String sectionIdList) {
		this.sectionIdList = sectionIdList;
	}

	public String getSiteId() {
		return siteId;
	}

	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getLaneId() {
		return laneId;
	}

	public void setLaneId(String laneId) {
		this.laneId = laneId;
	}

	
	
	
	
	
}
